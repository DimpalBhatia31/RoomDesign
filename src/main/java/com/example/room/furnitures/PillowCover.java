package com.example.room.furnitures;

import java.awt.Dimension;

public class PillowCover {

	private int id;

	private String colur;

	private Dimension dimesions;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getColur() {
		return colur;
	}

	public void setColur(String colur) {
		this.colur = colur;
	}

	public Dimension getDimesions() {
		return dimesions;
	}

	public void setDimesions(Dimension dimesions) {
		this.dimesions = dimesions;
	}

}
